def generateCaptcha(captchaInput, tokenDistance):
    inputLength = len(captchaInput)

    pairs = [(captchaInput[index], captchaInput[(index + tokenDistance) % inputLength]) for index in
             range(0, inputLength)]

    validTokens = [int(pair[0]) for pair in pairs if pair[0] == pair[1]]

    return sum(validTokens)


